function pick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

type TimeOfDay = 'morning' | 'afternoon' | 'evening' | 'night';

function timeOfDay(d: Date): TimeOfDay {
  const h = d.getHours();
  if (h >= 5 && h < 12) return 'morning';
  if (h >= 12 && h < 17) return 'afternoon';
  if (h >= 17 && h < 21) return 'evening';
  return 'night';
}

function pickTimePool(t: TimeOfDay): string[] {
  switch (t) {
    case 'morning': return MESSAGES_MORNING;
    case 'afternoon': return MESSAGES_AFTERNOON;
    case 'evening': return MESSAGES_EVENING;
    case 'night': return MESSAGES_NIGHT;
  }
}

export interface EncouragementContext {
  /** 현재 연속 미션 성공 일수 (선택). 마일스톤이면 우선 노출. */
  streak?: number;
  /** 메시지 추첨 기준 시각. 미지정시 new Date(). */
  now?: Date;
}

/**
 * 상황에 맞는 한국어 응원 메시지를 반환한다.
 * 항상 성공 (네트워크 호출 없음).
 *
 * 우선순위:
 *   1) 스트릭 마일스톤 (7/14/30/100) 정확히 일치 → 30%
 *   2) 시간대 풀 → 25%
 *   3) 진행률 풀 → 나머지
 */
export async function generateEncouragementText(
  completedQuestTitles: string[],
  totalQuestCount?: number,
  context?: EncouragementContext,
): Promise<string> {
  const completed = completedQuestTitles.length;
  const total = totalQuestCount ?? Math.max(completed, completed + 3);
  const ratio = total > 0 ? completed / total : 0;

  // 1) Streak milestone — 30% 확률로 노출
  const streak = context?.streak ?? 0;
  if (streak > 0 && Math.random() < 0.3) {
    if (streak >= 100 && streak % 100 === 0) return pick(STREAK_HUNDRED);
    if (streak === 30) return pick(STREAK_MONTH);
    if (streak === 14) return pick(STREAK_TWO_WEEKS);
    if (streak === 7) return pick(STREAK_WEEK);
  }

  // 2) 시간대 풀 — 25% 확률로 진행률 대신 시간대 메시지
  if (Math.random() < 0.25) {
    return pick(pickTimePool(timeOfDay(context?.now ?? new Date())));
  }

  // 3) 진행률 풀 (기본 경로)
  if (completed === 0) return pick(MESSAGES_NONE);
  if (ratio >= 1) return pick(MESSAGES_ALL);
  if (ratio >= 0.8) return pick(MESSAGES_MOST);
  if (ratio >= 0.4) return pick(MESSAGES_HALF);
  return pick(MESSAGES_FEW);
}
